// Sign the PRODUCTION rule set and write the publish migration.
//
// Reads the rule content from packages/core/rules/seed.json (the canonical rule set), signs
// {version, services} with the PRODUCTION key, and writes supabase/migrations/0006_prod_rule_set.sql
// (which flips the previous current set off and inserts this one as current). The payload + signature
// are PUBLIC — safe to commit; the signing private key is never written anywhere by this script.
//
// Private key resolution: STILL_PROD_PRIVATE_KEY_HEX env wins, else the gitignored file written by
// gen-rule-set-key.mjs. Version: STILL_PROD_VERSION env, else the seed version with the patch bumped
// (so it is strictly newer than the bundled seed and clients adopt it).
//
// Run:  pnpm --filter @still/core sign-prod-set       (after gen-rule-set-key + populating trusted-keys)

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
import * as ed from "@noble/ed25519";
import { bytesToHex, hexToBytes, utf8ToBytes } from "@noble/hashes/utils.js";

const KID = process.env.STILL_PROD_KID || "still-prod-1";
const here = dirname(fileURLToPath(import.meta.url));

// Resolve the private key: env first, then the gitignored local file.
const keyFile = join(here, "..", ".secrets", "rule-set-prod-key.local");
let privHex = process.env.STILL_PROD_PRIVATE_KEY_HEX?.trim();
if (!privHex && existsSync(keyFile)) privHex = readFileSync(keyFile, "utf8").trim();
if (!privHex) {
  console.error("No production private key found.");
  console.error("Set STILL_PROD_PRIVATE_KEY_HEX, or run `pnpm --filter @still/core gen-rule-set-key` first.");
  process.exit(1);
}

// Canonicalization — must match canonical.ts (sortDeep + JSON.stringify) byte-for-byte, or the
// client signature check fails.
function sortDeep(value) {
  if (Array.isArray(value)) return value.map(sortDeep);
  if (value !== null && typeof value === "object") {
    const out = {};
    for (const key of Object.keys(value).sort()) out[key] = sortDeep(value[key]);
    return out;
  }
  return value;
}
const canonicalize = (value) => JSON.stringify(sortDeep(value));

const seed = JSON.parse(readFileSync(join(here, "..", "rules", "seed.json"), "utf8"));
const bumpPatch = (v) => {
  const p = v.split(".").map(Number);
  p[2] = (p[2] || 0) + 1;
  return p.join(".");
};
const version = process.env.STILL_PROD_VERSION || bumpPatch(seed.version);
const payload = { version, services: seed.services };

const sig = await ed.signAsync(utf8ToBytes(canonicalize(payload)), hexToBytes(privHex));
const signature = { kid: KID, alg: "ed25519", value: bytesToHex(sig) };

// SQL literal with single-quote escaping (Postgres doubles single quotes inside string literals).
const lit = (obj) => `'${JSON.stringify(obj).replace(/'/g, "''")}'`;

const sql = `-- Publish the PRODUCTION-signed rule set as the current hosted set (R11 / KTD8).
-- Generated by packages/core/scripts/sign-prod-set.mjs — do NOT hand-edit. The payload + signature are
-- public (served to every client); the signing PRIVATE key is a deploy-only secret, never committed.
-- Apply to hosted Supabase with \`supabase db push\` (or paste into the SQL editor). At most one row may
-- be current (enforced in 0003), so flip the previous one off first.
update public.rule_sets set is_current = false where is_current = true;

insert into public.rule_sets (version, payload, signature, is_current)
values (
  '${version}',
  ${lit(payload)}::jsonb,
  ${lit(signature)}::jsonb,
  true
)
on conflict (version) do update
  set payload = excluded.payload, signature = excluded.signature, is_current = true;
`;

const migPath = join(here, "..", "..", "..", "supabase", "migrations", "0006_prod_rule_set.sql");
writeFileSync(migPath, sql);

console.log(`Signed the production rule set.`);
console.log(`  version : ${version}  (bundled seed is ${seed.version})`);
console.log(`  kid     : ${KID}`);
console.log(`  migration: supabase/migrations/0006_prod_rule_set.sql`);
console.log(`\nNext: apply it to hosted Supabase — \`supabase db push\` (linked project) or paste the SQL.`);
